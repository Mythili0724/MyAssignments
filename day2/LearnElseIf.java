package week1.day2;

public class LearnElseIf {
	public static void main(String[] args) {
	int a=10;
	int b=5;
	
	if(a>b)
	{
		System.out.println("true"+ " " + "a is greater than b");
	}
	else if(a<b)
	{
		System.out.println("false");
	}
	
	else
	{
		System.out.println(a+b);
	}

}
}
